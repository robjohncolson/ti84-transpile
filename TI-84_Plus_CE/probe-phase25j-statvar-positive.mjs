#!/usr/bin/env node

/**
 * Phase 25J: Rcl_StatVar positive-path probe.
 *
 * Goal:
 *   Seed the exact RAM slot that Rcl_StatVar reads for A=tMean (0x21),
 *   set statsValid, call the primitive impl at 0x08019F, and verify OP1
 *   matches the seeded value.
 *
 * Static address derivation from ti84pceg.inc + ROM.transpiled.js:
 *   statVarsOffset := 0xD0117F
 *   statVars       := 0xD01191
 *   tMean          := 0x21
 *   statFlags      := IY + 0x09
 *   statsValid     := bit 6
 *
 *   0x08019F  call 0x09A3A5   ; token -> source pointer in HL
 *   0x0801A3  call 0x07F9FB   ; DE=OP1, copy 9 bytes from HL to OP1
 *   0x0801A7  ret
 *
 *   0x09A3A5 normal path:
 *     HL = statVarsOffset + (9 * A)
 *
 * Therefore for A=tMean=0x21:
 *   source slot = 0xD0117F + (9 * 0x21) = 0xD012A8
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

import { createExecutor } from './cpu-runtime.js';
import { createPeripheralBus } from './peripherals.js';
import { writeReal, readReal } from './fp-real.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPORT_PATH = path.join(__dirname, 'phase25j-statvar-positive-report.md');

const romBytes = fs.readFileSync(path.join(__dirname, 'ROM.rom'));
const romModule = await import(pathToFileURL(path.join(__dirname, 'ROM.transpiled.js')).href);
const BLOCKS = romModule.PRELIFTED_BLOCKS;

const MEM_SIZE = 0x1000000;
const BOOT_ENTRY = 0x000000;
const BOOT_MODE = 'z80';
const BOOT_MAX_STEPS = 20000;
const BOOT_MAX_LOOP_ITER = 32;
const STACK_RESET_TOP = 0xD1A87E;
const KERNEL_INIT_ENTRY = 0x08C331;
const POST_INIT_ENTRY = 0x0802B2;

const IMPL_RCL_STATVAR = 0x08019F;
const CMP_STAT_PTR = 0x09A3A5;
const MOV9_TO_OP1 = 0x07F9FB;

const FLAGS_BASE = 0xD00080;
const STATFLAGS_ADDR = FLAGS_BASE + 0x09;
const STATS_VALID_MASK = 0x40;

const STAT_VARS_OFFSET_ADDR = 0xD0117F;
const STAT_VARS_ADDR = 0xD01191;
const OP1_ADDR = 0xD005F8;
const FP_REAL_LEN = 9;

const TMEAN_TOKEN = 0x21;
const TMEAN_SLOT_ADDR = STAT_VARS_OFFSET_ADDR + (TMEAN_TOKEN * FP_REAL_LEN);
const TMEAN_SLOT_FROM_STATVARS = TMEAN_SLOT_ADDR - STAT_VARS_ADDR;

const FAKE_RET = 0x7FFFFE;
const INSN_BUDGET = 200000;

const SEED_VALUE = 42.0;
const TOLERANCE = 1e-6;
const OP1_SENTINEL = 0xCC;

function hex(value, width = 6) {
  if (value === undefined || value === null) return 'n/a';
  return `0x${(value >>> 0).toString(16).padStart(width, '0')}`;
}

function wrapMem(mem) {
  return {
    write8(addr, val) { mem[addr] = val & 0xFF; },
    read8(addr) { return mem[addr] & 0xFF; },
  };
}

function readBlock(mem, addr, len) {
  const out = new Uint8Array(len);
  for (let i = 0; i < len; i++) out[i] = mem[addr + i];
  return out;
}

function hexBytes(bytes) {
  return Array.from(bytes, (b) => b.toString(16).padStart(2, '0')).join(' ');
}

function bytesEqual(a, b) {
  if (a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) {
    if (a[i] !== b[i]) return false;
  }
  return true;
}

function coldBoot(executor, cpu, mem) {
  const result = executor.runFrom(BOOT_ENTRY, BOOT_MODE, {
    maxSteps: BOOT_MAX_STEPS,
    maxLoopIterations: BOOT_MAX_LOOP_ITER,
  });

  cpu.halted = false;
  cpu.iff1 = 0;
  cpu.iff2 = 0;
  cpu.sp = STACK_RESET_TOP - 3;
  mem.fill(0xFF, cpu.sp, cpu.sp + 3);

  executor.runFrom(KERNEL_INIT_ENTRY, 'adl', {
    maxSteps: 100000,
    maxLoopIterations: 10000,
  });

  cpu.mbase = 0xD0;
  cpu._iy = 0xD00080;
  cpu._hl = 0;
  cpu.halted = false;
  cpu.iff1 = 0;
  cpu.iff2 = 0;
  cpu.sp = STACK_RESET_TOP - 3;
  mem.fill(0xFF, cpu.sp, cpu.sp + 3);

  executor.runFrom(POST_INIT_ENTRY, 'adl', {
    maxSteps: 100,
    maxLoopIterations: 32,
  });

  return result;
}

function postInitState(cpu, mem) {
  cpu.halted = false;
  cpu.iff1 = 0;
  cpu.iff2 = 0;
  cpu.madl = 1;
  cpu._iy = 0xD00080;
  cpu.f = 0x40;
  cpu._ix = 0xD1A860;
  cpu.sp = STACK_RESET_TOP - 12;
  mem.fill(0xFF, cpu.sp, cpu.sp + 12);
}

function writeReport(details) {
  const lines = [];
  const fmt = (ok) => (ok ? 'PASS' : 'FAIL');

  lines.push('# Phase 25J - Rcl_StatVar positive-path probe');
  lines.push('');
  lines.push('Generated by `probe-phase25j-statvar-positive.mjs`.');
  lines.push('');
  lines.push('## Goal');
  lines.push('');
  lines.push('Seed the exact RAM slot selected by `Rcl_StatVar` for `A=tMean`, set');
  lines.push('`statsValid`, call the primitive impl at `0x08019F`, and verify the');
  lines.push('copied OP1 value matches the seeded real.');
  lines.push('');
  lines.push('## Static address derivation');
  lines.push('');
  lines.push('- `statVarsOffset = 0xD0117F`');
  lines.push('- `statVars = 0xD01191`');
  lines.push('- `tMean = 0x21`');
  lines.push(`- \`CmpStatPtr\` @ \`${hex(CMP_STAT_PTR)}\` uses \`HL = statVarsOffset + 9*A\``);
  lines.push(`- \`Mov9ToOP1\` @ \`${hex(MOV9_TO_OP1)}\` copies 9 bytes from HL into OP1`);
  lines.push(`- Derived tMean source slot: \`${hex(TMEAN_SLOT_ADDR)}\``);
  lines.push(`- Relative to \`statVars\`: \`${hex(TMEAN_SLOT_FROM_STATVARS)}\` bytes`);
  lines.push('');
  lines.push('## Runtime setup');
  lines.push('');
  lines.push(`- Seed value: ${SEED_VALUE}`);
  lines.push(`- Seed slot before: \`${details.seedBeforeHex}\``);
  lines.push(`- Seed slot after:  \`${details.seedHex}\``);
  lines.push(`- statsValid byte: \`${hex(details.statFlagsBefore, 2)}\` -> \`${hex(details.statFlagsArmed, 2)}\``);
  lines.push(`- OP1 pre-call sentinel: \`${details.op1BeforeHex}\``);
  lines.push('');
  lines.push('## Call');
  lines.push('');
  lines.push(`- Entry: \`${hex(IMPL_RCL_STATVAR)}\``);
  lines.push(`- Fake return: \`${hex(FAKE_RET)}\``);
  lines.push(`- Instruction budget: ${INSN_BUDGET}`);
  lines.push(`- Blocks dispatched: ${details.blockCount}`);
  lines.push(`- Final PC: \`${hex(details.finalPc)}\``);
  lines.push(`- Returned to fake return: ${details.returnHit}`);
  if (details.recentPcs.length > 0) {
    lines.push(`- Recent PCs: ${details.recentPcs.map((pc) => hex(pc)).join(' ')}`);
  }
  lines.push('');
  lines.push('## Result');
  lines.push('');
  lines.push(`- OP1 post-call: \`${details.op1Hex}\``);
  lines.push(`- OP1 decoded value: ${details.got}`);
  lines.push(`- Absolute diff vs ${SEED_VALUE}: ${details.diff}`);
  lines.push('');
  lines.push('## Assertions');
  lines.push('');
  lines.push(`- statsValid set before call: **${fmt(details.statsValidSet)}**`);
  lines.push(`- seed slot round-trips through readReal: **${fmt(details.seedRoundTrip)}**`);
  lines.push(`- call returned to fake return: **${fmt(details.returnHit)}**`);
  lines.push(`- OP1 raw bytes equal seeded bytes: **${fmt(details.rawMatch)}**`);
  lines.push(`- OP1 numeric value within tolerance: **${fmt(details.valueMatch)}**`);
  lines.push('');
  lines.push('## Interpretation');
  lines.push('');
  lines.push(details.pass
    ? '- Overall: PASS. The primitive copied the seeded tMean slot into OP1.'
    : '- Overall: FAIL. At least one positive-path assertion did not hold.'
  );
  lines.push('- Note: the ROM helper uses `statVarsOffset`, not `statVars`, as the');
  lines.push('  base when converting the token to the source pointer.');

  fs.writeFileSync(REPORT_PATH, `${lines.join('\n')}\n`);
}

async function main() {
  console.log('=== Phase 25J: Rcl_StatVar positive-path probe ===');

  const mem = new Uint8Array(MEM_SIZE);
  mem.set(romBytes);

  const peripherals = createPeripheralBus({ pllDelay: 2, timerInterrupt: false });
  const executor = createExecutor(BLOCKS, mem, { peripherals });
  const cpu = executor.cpu;

  const bootResult = coldBoot(executor, cpu, mem);
  console.log(`boot: steps=${bootResult.steps} term=${bootResult.termination} lastPc=${hex(bootResult.lastPc)}`);

  postInitState(cpu, mem);

  const memWrap = wrapMem(mem);
  const seedBefore = readBlock(mem, TMEAN_SLOT_ADDR, FP_REAL_LEN);

  writeReal(memWrap, TMEAN_SLOT_ADDR, SEED_VALUE);
  const seedBytes = readBlock(mem, TMEAN_SLOT_ADDR, FP_REAL_LEN);
  const seededRoundTrip = readReal(memWrap, TMEAN_SLOT_ADDR);

  const statFlagsBefore = mem[STATFLAGS_ADDR] & 0xFF;
  mem[STATFLAGS_ADDR] = statFlagsBefore | STATS_VALID_MASK;
  const statFlagsArmed = mem[STATFLAGS_ADDR] & 0xFF;

  for (let i = 0; i < FP_REAL_LEN; i++) mem[OP1_ADDR + i] = OP1_SENTINEL;
  const op1Before = readBlock(mem, OP1_ADDR, FP_REAL_LEN);

  cpu.a = TMEAN_TOKEN;
  cpu.madl = 1;
  cpu.push(FAKE_RET);

  let finalPc = null;
  let blockCount = 0;
  let returnHit = false;
  const recentPcs = [];

  try {
    const result = executor.runFrom(IMPL_RCL_STATVAR, 'adl', {
      maxSteps: INSN_BUDGET,
      maxLoopIterations: 4096,
      onBlock(pc) {
        const norm = pc & 0xFFFFFF;
        cpu.pc = norm;
        finalPc = norm;
        blockCount++;
        recentPcs.push(norm);
        if (recentPcs.length > 10) recentPcs.shift();
        if (norm === FAKE_RET) throw new Error('__RETURN_HIT__');
      },
      onMissingBlock(pc) {
        const norm = pc & 0xFFFFFF;
        cpu.pc = norm;
        finalPc = norm;
        if (norm === FAKE_RET) throw new Error('__RETURN_HIT__');
      },
    });
    finalPc = result.lastPc ?? finalPc;
    console.log(`call done: steps=${result.steps} term=${result.termination} lastPc=${hex(result.lastPc)}`);
  } catch (err) {
    if (err?.message === '__RETURN_HIT__') {
      returnHit = true;
      finalPc = FAKE_RET;
      console.log(`call returned to FAKE_RET @ ${hex(FAKE_RET)}`);
    } else {
      throw err;
    }
  }

  const op1After = readBlock(mem, OP1_ADDR, FP_REAL_LEN);
  const got = readReal(memWrap, OP1_ADDR);
  const diff = Math.abs(got - SEED_VALUE);
  const statsValidSet = (statFlagsArmed & STATS_VALID_MASK) !== 0;
  const seedRoundTrip = Math.abs(seededRoundTrip - SEED_VALUE) <= TOLERANCE;
  const rawMatch = bytesEqual(seedBytes, op1After);
  const valueMatch = diff <= TOLERANCE;
  const pass = statsValidSet && seedRoundTrip && returnHit && rawMatch && valueMatch;

  console.log(`seed slot @ ${hex(TMEAN_SLOT_ADDR)} [${hexBytes(seedBytes)}]`);
  console.log(`OP1 post-call [${hexBytes(op1After)}]`);
  console.log(`got=${got} expected=${SEED_VALUE} diff=${diff}`);
  console.log(pass ? 'PASS' : 'FAIL');

  writeReport({
    statFlagsBefore,
    statFlagsArmed,
    seedBeforeHex: hexBytes(seedBefore),
    seedHex: hexBytes(seedBytes),
    op1BeforeHex: hexBytes(op1Before),
    op1Hex: hexBytes(op1After),
    blockCount,
    finalPc,
    returnHit,
    recentPcs,
    got,
    diff,
    statsValidSet,
    seedRoundTrip,
    rawMatch,
    valueMatch,
    pass,
  });

  console.log(`report=${REPORT_PATH}`);
  process.exitCode = pass ? 0 : 1;
}

try {
  await main();
} catch (error) {
  console.error(error.stack || error);
  fs.writeFileSync(
    REPORT_PATH,
    `# Phase 25J - FAILED\n\n\`\`\`\n${error.stack || error}\n\`\`\`\n`,
  );
  process.exitCode = 1;
}
